package cn.jxufe.db.domain;

public class tank {
	private String tankclassno;
	private String tankclassname;
	private int tankprice;
	public String getTankclassno() {
		return tankclassno;
	}
	public void setTankclassno(String tankclassno) {
		this.tankclassno = tankclassno;
	}
	public String getTankclassname() {
		return tankclassname;
	}
	public void setTankclassname(String tankclassname) {
		this.tankclassname = tankclassname;
	}
	public int getTankprice() {
		return tankprice;
	}
	public void setTankprice(int tankprice) {
		this.tankprice = tankprice;
	}
}
